    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (lesson == null || !lesson.hasQuiz()) {
            JOptionPane.showMessageDialog(this, "No quiz available for this lesson");
            return;
        }

        Quiz quiz = lesson.getQuiz();
        if (quiz == null) {
            JOptionPane.showMessageDialog(this, "Quiz data not available");
            return;
        }
        // Open QuizPage
        QuizPage quizPage = new QuizPage(quiz, student, quizService, dbManager, this, lesson.getLessonId());
        quizPage.setVisible(true);

        // Optional: disable the take quiz button while quiz is open
        jButton2.setEnabled(false);
 
    }//GEN-LAST:event_jButton2ActionPerformed

