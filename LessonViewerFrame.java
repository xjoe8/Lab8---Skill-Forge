    private void loadLessonInfo() {
    jLabel1.setText(lesson.getTitle());
    txtContent.setText(lesson.getContent());
    txtContent.setCaretPosition(0);

    List<String> resources = lesson.getResources();
    txtResources.setText(resources.isEmpty() ? "No resources available." : String.join("\n", resources));
    
    // Use existing methods instead of isLessonCompleted
    if (student.getCompletedLessons(course.getCourseId()).contains(lesson.getLessonId())) {
        jButton2.setEnabled(false); // mark complete button
         jButton2.setText("Already Completed");
    } else {
        jButton2.setEnabled(true);
    }
}
    
    public void onQuizCompleted(boolean passed, String lessonId) {
    if (passed) {
        // The lesson is already marked as completed in the Student object
        // Just update the UI
        JOptionPane.showMessageDialog(this, "Lesson completed! You passed the quiz.");
        jButton2.setEnabled(false);
        
    } else {
        JOptionPane.showMessageDialog(this, "Quiz failed. Please try again.");
    }
}
