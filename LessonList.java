    private void loadintoTable(Course course) {
        String courseId = course.getCourseId();

        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            List<Lesson> lessons = new ArrayList<>();
            lessons = lessonService.getlessonsByCourse(courseId);
            System.out.println("Loading lessons for courseId = " + courseId);
            System.out.println("Found lessons: " + lessons.size());

            for (Lesson lesson : lessons) {
                // Check if lesson has quiz
                boolean hasQuiz = lesson.hasQuiz();
                String quizStatus = hasQuiz ? "Has Quiz" : "No Quiz";

                // Check if student has completed this lesson
                boolean isCompleted = isLessonCompleted(lesson.getLessonId());
                String completionStatus = isCompleted ? "âœ“ Completed" : "Not Started";

                model.addRow(new Object[]{
                    lesson.getLessonId(),
                    lesson.getTitle(),
                    quizStatus,
                    completionStatus
                });
            }

            if (lessons.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No lessons found for this course.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading lessons: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private boolean isLessonCompleted(String lessonId) {
        // Check if this lesson is in student's completed lessons
        List<String> completedLessons = student.getCompletedLessons(course.getCourseId());
        return completedLessons != null && completedLessons.contains(lessonId);
    }

    public void refreshLessonList() {
        loadintoTable(course);
    }

