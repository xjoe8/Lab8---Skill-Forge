    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: view lesson button
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Get lesson ID from the selected row (column 0)
            String lessonId = jTable1.getValueAt(selectedRow, 0).toString();
            String lessonTitle = jTable1.getValueAt(selectedRow, 1).toString();

            System.out.println("Button action on selected row - Lesson ID: " + lessonId + ", Title: " + lessonTitle);

            // Use the service method to get the lesson
            Lesson selectedLesson = lessonService.getLessonById(course.getCourseId(), lessonId);

            if (selectedLesson != null) {
                // Open the lesson viewer with the selected lesson
                LessonViewerFrame lessonViewer = new LessonViewerFrame(student, course, selectedLesson,dbManager);
                lessonViewer.setVisible(true);

                // Optional: Add listener to refresh when lesson viewer closes
                lessonViewer.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosed(java.awt.event.WindowEvent e) {
                        // Refresh the table to show updated completion status
                        loadintoTable(course);
                    }
                });
            } else {
                JOptionPane.showMessageDialog(this, "Error: Could not load lesson details.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a lesson to view.");
            System.out.println("No row selected.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

